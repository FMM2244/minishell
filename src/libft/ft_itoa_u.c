#include "libft.h"
#include "ft_printf.h"

int	ft_itoa_u(unsigned int n)
{
	int	i;

	i = 0;
	if (n / 10)
		i += ft_itoa_u(n / 10);
	ft_putchar_fd((n % 10) + 48, 1);
	return (i + 1);
}
